let arr = [];
//add new item to the end of an array 
arr.push(1);
arr.push(2);
arr.push(3);

console.log(arr);

//removes element from the end
arr.pop();
arr.pop();

console.log(arr);

//add to the beginning
arr.unshift(5);
arr.unshift(6);

console.log(arr);

//remove first element
arr.shift();
console.log(arr);

